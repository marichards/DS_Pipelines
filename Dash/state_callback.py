import dash
import dash_core_components as dcc
import dash_html_components as html
from dash.dependencies import Input, Output, State

external_stylesheets = ['https://codepen.io/chriddyp/pen/bWLwgP.css']

app = dash.Dash(__name__, external_stylesheets=external_stylesheets)

app.layout = html.Div([
    # Create 2 text inputs with Montreal and Canada as initial values
    dcc.Input(id='input-1-state', type='text', value='Montr√©al'),
    dcc.Input(id='input-2-state', type='text', value='Canada'),
    # Create a submit button; it has a number of clicks associated with it and it auto-increments
    html.Button(id='submit-button', n_clicks=0, children='Submit'),
    # Make a place for text to go
    html.Div(id='output-state')
])

# Take in the output space, the submit button as the input (reactive bit) and the text as states
@app.callback(Output('output-state', 'children'),
              [Input('submit-button', 'n_clicks')],
              [State('input-1-state', 'value'),
               State('input-2-state', 'value')])
# Use, in order, the number of clicks, the city value, and the country value
def update_output(n_clicks, input1, input2):
    return u'''
        The Button has been pressed {} times,
        Input 1 is "{}",
        and Input 2 is "{}"
    '''.format(n_clicks, input1, input2)


if __name__ == '__main__':
    app.run_server(debug=True)